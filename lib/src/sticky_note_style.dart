/// Visual style presets for the sticky note background.
enum StickyNoteStyle {
  /// Solid color with a subtle vertical gradient.
  classic,

  /// Yellow legal pad style with horizontal blue lines and a red margin.
  lined,

  /// Engineering/math style with a light gray grid of squares.
  grid,

  /// Custom ImageProvider overlay (crinkled paper, etc.).
  textured,

  /// Scattered small dots pattern.
  dotted,

  /// Diagonal cross-hatched pencil lines.
  crosshatch,

  /// Kraft/recycled brown paper with speckled noise.
  kraft,

  /// Blueprint style: dark blue background with white grid lines.
  blueprint,

  /// Cork board: warm brown with darker fiber-like speckles.
  cork,

  /// Linen/canvas: subtle woven horizontal and vertical threads.
  linen,
}
